/** Get a demo by its ID */
export function getDemo(id: string): Demo | undefined {
  return demos.find((d) => d.id === id);
}

/** Get the index of a demo by ID */
export function getDemoIndex(id: string): number {
  return demos.findIndex((d) => d.id === id);
}

/** Get next/prev demo IDs for navigation */
export function getDemoNavigation(id: string): { prev: Demo | null; next: Demo | null } {
  const idx = getDemoIndex(id);
  return {
    prev: idx > 0 ? demos[idx - 1] : null,
    next: idx < demos.length - 1 ? demos[idx + 1] : null,
  };
}
